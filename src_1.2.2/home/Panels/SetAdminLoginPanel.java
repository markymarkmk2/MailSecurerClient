    private void BT_OKActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_BT_OKActionPerformed
    {//GEN-HEADEREND:event_BT_OKActionPerformed
        // TODO add your handling code here:
        boolean strong = false;
        String pwd = new String(PF_PWD.getPassword());
        String pwd1 = new String(PF_PWD1.getPassword());

        if (!CheckPwdPanel.check_passwords(my_dlg, pwd, pwd1, strong))
            return;

        String name = TXT_USER.getText();
        if (!Validator.is_valid_user(name))
        {
            UserMain.errm_ok(my_dlg, UserMain.Txt("The_username_ist_too_short") );
            return;
        }
        
        String ret = UserMain.fcc().call_abstract_function("GETSETOPTION CMD:SETADMIN NAME:" + name + " VAL:" + pwd, ServerCall.SHORT_CMD_TO);
        if (ret == null || ret.charAt(0) != '0')
        {
            UserMain.errm_ok(my_dlg, UserMain.Txt("The_new_userdata_could_not_be_set") + (ret != null ? ": " + ret : "") );
            return;
        }
            
        this.setVisible(false);
    }//GEN-LAST:event_BT_OKActionPerformed

