    private static final long _1M = 1024*1024;

    private boolean  handle_status()
    {
        String ret = UserMain.fcc().call_abstract_function("backup CMD:status MA:" + object.getMandant().getId() + " DA:" + object.getDiskArchive().getId() + " BS:" + object.getId());
        if (ret == null || ret.charAt(0) != '0')
        {
            return false;
        }

        // "ID:%ld TB:%.0lf CB:%.0lf TF:%ld TD:%ld CF:%ld CD:%ld SP:%.0lf STF:%.0lf SCF:%.0lf LT:%.0lf PC:%d",

        SizeStr str = new SizeStr(0);
        ParseToken pt = new ParseToken(ret);
        if (pt.GetLong("ID:") != 9999)
        {
            long next_start = pt.GetLong("NS:");
            if (next_start > 0)
            {
                Date d = new Date(next_start * 1000);
                SimpleDateFormat sdf = new SimpleDateFormat("HH:mm:ss  dd.MM.yyyy");
                TXT_NEXT_START.setText( sdf.format(d));
            }
            
            boolean last_result_ok = pt.GetBoolean("LOK:");
            boolean last_result_nok = pt.GetBoolean("LNOK:");
            ImageIcon img = null;
            if (last_result_ok)
                img = ok_icon;
            else if (last_result_nok)
                img = nok_icon;

            if (LB_ACTIVE1.getIcon() != img)
            {
                LB_ACTIVE1.setIcon(img);
            }



            return false;
        }

        LB_ACTIVE1.setIcon(null);


        TXT_TB.setText( pt.GetLong("TB:").longValue()  + " MB" );
        TXT_CB.setText( pt.GetLong("CB:").longValue()  + " MB" );
        TXT_CD.setText( pt.GetLong("CD:").toString() );
        TXT_TD.setText( pt.GetLong("TD:").toString() );
        TXT_TF.setText( pt.GetLong("TF:").toString() );
        TXT_CF.setText( pt.GetLong("CF:").toString() );

        str.set_size( pt.GetDoubleValue("SP:"));
        TXT_SP.setText( str.toString()  );
        str.set_size( pt.GetDoubleValue("LT:"));
        TXT_LT.setText( str.toString() );
        TXT_SPF.setText( pt.GetLong("STF:").toString() );

        return true;
    }

