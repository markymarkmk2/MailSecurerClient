    @Override
    public void actionPerformed( ActionEvent e )
    {
        timer.stop();

        try
        {
            if (handle_status())
            {
                LB_ACTIVE.setIcon(ok_icon); 
            }
            else
            {
                LB_ACTIVE.setIcon(offline_icon); 
            }


        }
        catch (Exception exc)
        {
        }
        timer.start();

    }

